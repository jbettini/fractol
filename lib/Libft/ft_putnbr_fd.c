#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n != -2147483648)
	{
		if (n < 0)
		{
			ft_putchar_fd('-', fd);
			n *= -1;
		}
		if (n > 9)
			ft_putnbr_fd(n / 10, fd);
		n = n % 10 + 48;
		ft_putchar_fd(n, fd);
	}
	else
		write(fd, "-2147483648", 11);
}
